<?php
$a = 10;
$b = 5;

// Suma
$suma = $a + $b; // $suma será igual a 15

// Resta
$resta = $a - $b; // $resta será igual a 5

// Multiplicación
$multiplicacion = $a * $b; // $multiplicacion será igual a 50

// División
$division = $a / $b; // $division será igual a 2

// Módulo (resto de la división)
$modulo = $a % $b; // $modulo será igual a 0

// Incremento
$a++; // Ahora $a es igual a 11

// Decremento
$b--; // Ahora $b es igual a 4